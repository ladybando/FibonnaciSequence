public class Triangles {

    public static void main(String[] args) {

        printTriangles(); // calls the method to print the triangles

    }

    // this method prints two triangles to the screen
    public static void printTriangles() {

    System.out.println("#");
    System.out.println("##");
    System.out.println("###");
    System.out.println("####");
    System.out.println("#####");
    //line break between the two triangles
    System.out.println();
    //start of new triangle
    System.out.println("    #");
    System.out.println("   ##");
    System.out.println("  ###");
    System.out.println(" ####");
    System.out.println("#####");
    }

}


